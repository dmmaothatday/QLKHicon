    public static void loadTableChiTietDay(ArrayList<SanPhamModel> ctd){
        tb.getDataVector().removeAllElements();
        for(int i=0; i < ctd.size(); i++){
            String[] d = {ctd.get(i).getMaSP(), Integer.toString(ctd.get(i).getSoLuong()), ctd.get(i).getGhiChu()};
            tb.addRow(d);
            tbChiTietDay.setModel(tb);
        }
    }
    
