    private void tbChiTietDayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbChiTietDayMouseClicked
        // TODO add your handling code here:
        boolean checkClick = tbChiTietDay.isEditing();
        if (checkClick == false){
            JTable target = (JTable) evt.getSource();
            int rowIndex = target.getSelectedRow();
            txtMaSanPham.setText((String)tbChiTietDay.getValueAt(rowIndex,0));
            spnSoLuong.setValue(Integer.valueOf((String)tbChiTietDay.getValueAt(rowIndex,1)));
            txtGhiChu.setText((String)tbChiTietDay.getValueAt(rowIndex,2));
            txtMaSanPham.setEnabled(false);
            check = true;
        }
    }//GEN-LAST:event_tbChiTietDayMouseClicked

