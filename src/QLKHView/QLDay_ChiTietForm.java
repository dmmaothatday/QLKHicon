    public static SanPhamModel getChiTietDayInput(){
       SanPhamModel sp = new SanPhamModel();
       sp.setMaSP(txtMaSanPham.getText());
       sp.setSoLuong((int)spnSoLuong.getValue());
       sp.setGhiChu(txtGhiChu.getText());
       return sp;
    }
    
